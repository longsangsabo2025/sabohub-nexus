interface ParsedTask {
  id: string;
  title: string;
  description: string;
  category: string;
  priority: 'low' | 'medium' | 'high' | 'urgent';
  estimatedHours: number;
  suggestedEmployee: {
    id: string;
    name: string;
    role: string;
    currentWorkload: number;
    matchScore: number;
    reason: string;
  } | null;
  instructions: string[];
  dueDate: string;
  status: 'pending' | 'approved' | 'rejected';
}

interface Employee {
  id: string;
  full_name: string;
  role: string;
  currentTasks: number;
}

/**
 * AI TASK DELEGATOR - Elon Musk Philosophy
 * 
 * "The best interface is no interface" - AI should understand natural language
 * "Automate everything" - From parsing to assignment to tracking
 * "Speed matters" - From CEO idea to employee task in 30 seconds
 * 
 * WORKFLOW:
 * 1. CEO pastes natural language task list
 * 2. AI parses, categorizes, suggests best employee
 * 3. CEO reviews & approves (1-click)
 * 4. Tasks auto-assigned with instructions
 * 5. Real-time progress tracking
 */
